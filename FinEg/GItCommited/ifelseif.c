#include <stdio.h>

int main()
{
	double x = -1; // initialize variable //
    if (x < 0)
	{
		printf("x is negative\n");
	}
    else if (x == 0)
	{
		printf("x is zero\n");
	}
	else if (x ==1)
	{
		printf("x is one\n");
	}
	else if (x > 2)
	{
		printf("x is more than one\n");
	}
    
    return 0;
}
